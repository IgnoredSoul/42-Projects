#include "Fixed.hpp"

int main(void) 
{
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	Fixed a;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	Fixed const b( Fixed( 5.05f ) * Fixed( 2 ) );
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;

	std::cout << "a: " << a << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	std::cout << "++a: " << ++a << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	std::cout << "a: " << a << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	std::cout << "a++: " << a++ << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	std::cout << "a: " << a << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	std::cout << "b: " << b << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	std::cout << "comparing a & b: " << Fixed::max( a, b ) << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	return 0;
}