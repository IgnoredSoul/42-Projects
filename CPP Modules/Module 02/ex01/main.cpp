#include "Fixed.hpp"

int main( void ) 
{
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	Fixed a;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	Fixed const b( 10 );
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	Fixed const c( 42.42f );
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;
	Fixed const d( b );
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;

	a = Fixed( 1234.4321f );
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;

	std::cout << "a = " << a << std::endl;
	std::cout << "b = " << b << std::endl;
	std::cout << "c = " << c << std::endl;
	std::cout << "d = " << d << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;

	std::cout << "a = " << a.toInt() << std::endl;
	std::cout << "b = " << b.toInt() << std::endl;
	std::cout << "c = " << c.toInt() << std::endl;
	std::cout << "d = " << d.toInt() << std::endl;
	std::cout << "───────────────────────────────────────────────────────────────────────" << std::endl;

	return 0;
}